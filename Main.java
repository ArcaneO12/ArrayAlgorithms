public class Main {
    public static void main(String[] args) {
        int[] arr = { 1, 2, 3, 4, 5 };
        int sum = 0;

        for (int num : arr) {
            System.out.print(num + " ");
            sum += num;
        }
        System.out.println("\nSum: " + sum);
    }
}

